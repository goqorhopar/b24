import { GoogleGenerativeAI } from "@google/generative-ai";
import logger from "./logger.js";

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const generationConfig = {
  temperature: 0.7,
  topP: 0.8,
  topK: 40,
  maxOutputTokens: 2048,
};

const model = genAI.getGenerativeModel({ 
  model: "gemini-1.5-flash",
  generationConfig 
});

export async function analyzeTranscript(transcript) {
  // Валидация входных данных
  if (!transcript || transcript.trim().length < 50) {
    throw new Error('Транскрипт слишком короткий для анализа');
  }
  
  // Санитаризация транскрипта
  const safeTranscript = transcript
    .replace(/`/g, "'")
    .replace(/```/g, "'''")
    .slice(0, 30000);
  
  logger.info(`Анализируем транскрипт длиной ${transcript.length} символов`);

  const prompt = `
# ЗАДАЧА: Анализ транскрипта встречи

## РОЛЬ
Ты - эксперт по продажам и переговорам. Анализируешь транскрипции встреч и выдаешь структурированные саммари.

## ИНСТРУКЦИИ
В начале ответа всегда указывай имя клиента в формате: "Клиент: [Имя Фамилия]"

## ПУНКТЫ ДЛЯ АНАЛИЗА
1. АНАЛИЗ ТЕКУЩЕГО БИЗНЕСА КЛИЕНТА (чем занимается, основные продукты/услуги, ниша)
2. ВЫЯВЛЕНИЕ БОЛЕЙ И ПОТРЕБНОСТЕЙ (какие проблемы хочет решить клиент, боли бизнеса)
3. ВОЗРАЖЕНИЯ ПО ЛИДОГЕНЕРАЦИИ (какие сомнения высказал клиент, возражения)
4. РЕАКЦИЯ НА МОДЕЛЬ ГЕНЕРАЦИИ ЦЕЛЕВЫХ КЛИЕНТОВ
5. ОСОБЫЙ ИНТЕРЕС К СЕРВИСУ (что конкретно заинтересовало, приоритеты клиента)
6. НАЙДЕННЫЕ ВОЗМОЖНОСТИ (потенциальные точки роста, дополнительные услуги)
7. ОШИБКИ МЕНЕДЖЕРА (что можно было сделать лучше в продаже)
8. ПУТЬ К ЗАКРЫТИЮ (конкретные шаги к продаже, этапы сделки)
9. ТОН БЕСЕДЫ (настроение клиента, уровень вовлеченности, эмоции)
10. КОНТРОЛЬ ДИАЛОГА (кто управлял разговором, баланс в диалоге)
11. РЕКОМЕНДАЦИИ (конкретные действия для менеджера, план работы)
12. КАТЕГОРИЯ КЛИЕНТА (холодный/теплый/горячий, потенциальный объем сделки, бюджет)

## ДОПОЛНИТЕЛЬНАЯ ИНФОРМАЦИЯ
- Определи основной продукт/услугу, которую обсуждали
- Предложи 3 конкретных следующих шага с приоритетами
- Оцени вероятность сделки в процентах
- Укажи предполагаемый бюджет клиента если он упоминался
- Определи отрасль клиента (IT, ритейл, производство и т.д.)
- Выяви ключевых лиц, принимающих решения
- Определи сроки принятия решения если они обсуждались

## ФОРМАТ ВЫВОДА
Используй Markdown разметку для структурирования ответа. В конце отчета добавь разделы:
- СЛЕДУЮЩИЕ ШАГИ: (с конкретными задачами и сроками)
- ПРИОРИТЕТНЫЕ ДЕЙСТВИЯ: (самые важные задачи на сейчас)
- ДЕДЛАЙНЫ: (сроки по каждому этапу)
- КОНТАКТЫ: (если упоминались в разговоре)
- БЮДЖЕТ: (если обсуждался)
- ВЕРОЯТНОСТЬ СДЕЛКИ: (в процентах)

Без воды, только факты и рекомендации.

## ТРАНСКРИПТ
${safeTranscript}
`;

  try {
    const result = await model.generateContent(prompt);
    const text = result.response.text();
    logger.info("✅ Отчет получен от Gemini");
    return text;
  } catch (err) {
    logger.error(`Ошибка при анализе Gemini: ${err.message}`, { 
      errorCode: err.code,
      stack: err.stack 
    });
    throw err;
  }
}
