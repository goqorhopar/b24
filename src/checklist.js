import { GoogleGenerativeAI } from "@google/generative-ai";
import logger from "./logger.js";

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const model = genAI.getGenerativeModel({ model: "gemini-1.5-flash" });

export async function analyzeTranscript(transcript) {
  const safeTranscript = transcript.replace(/`/g, "'");
  
  const prompt = "Ты - эксперт по продажам и переговорам. Анализируешь транскрипции встреч и " +
    "выдаешь структурированные саммари.\n\n" +
    "В начале ответа всегда указывай имя клиента в формате: \"Клиент: [Имя Фамилия]\"\n\n" +
    "ЗАДАЧА:\n" +
    "Проанализировать транскрипт и выдать отчет по 12 пунктам:\n\n" +
    "1. АНАЛИЗ ТЕКУЩЕГО БИЗНЕСА КЛИЕНТА (чем занимается, основные продукты/услуги, ниша)\n" +
    "2. ВЫЯВЛЕНИЕ БОЛЕЙ И ПОТРЕБНОСТЕЙ (какие проблемы хочет решить клиент, боли бизнеса)\n" +
    "3. ВОЗРАЖЕНИЯ ПО ЛИДОГЕНЕРАЦИИ (какие сомнения высказал клиент, возражения)\n" +
    "4. РЕАКЦИЯ НА МОДЕЛЬ ГЕНЕРАЦИИ ЦЕЛЕВЫХ КЛИЕНТОВ\n" +
    "5. ОСОБЫЙ ИНТЕРЕС К СЕРВИСУ (что конкретно заинтересовало, приоритеты клиента)\n" +
    "6. НАЙДЕННЫЕ ВОЗМОЖНОСТИ (потенциальные точки роста, дополнительные услуги)\n" +
    "7. ОШИБКИ МЕНЕДЖЕРА (что можно было сделать лучше в продаже)\n" +
    "8. ПУТЬ К ЗАКРЫТИЮ (конкретные шаги к продаже, этапы сделки)\n" +
    "9. ТОН БЕСЕДЫ (настроение клиента, уровень вовлеченности, эмоции)\n" +
    "10. КОНТРОЛЬ ДИАЛОГА (кто управлял разговором, баланс в диалоге)\n" +
    "11. РЕКОМЕНДАЦИИ (конкретные действия для менеджера, план работы)\n" +
    "12. КАТЕГОРИЯ КЛИЕНТА (холодный/теплый/горячий, потенциальный объем сделки, бюджет)\n\n" +
    "ДОПОЛНИТЕЛЬНО:\n" +
    "- Определи основной продукт/услугу, которую обсуждали\n" +
    "- Предложи 3 конкретных следующих шага с приоритетами\n" +
    "- Оцени вероятность сделки в процентах\n" +
    "- Укажи предполагаемый бюджет клиента если он упоминался\n" +
    "- Определи отрасль клиента (IT, ритейл, производство и т.д.)\n" +
    "- Выяви ключевых лиц, принимающих решения\n" +
    "- Определи сроки принятия решения если они обсуждались\n\n" +
    "ТРАНСКРИПТ:\n" +
    safeTranscript + "\n\n" +
    "ФОРМАТ ВЫВОДА:\n" +
    "Структурированный отчет по каждому пункту с конкретными примерами из транскрипта.\n" +
    "В конце отчета добавь разделы:\n" +
    "СЛЕДУЮЩИЕ ШАГИ: (с конкретными задачами и сроками)\n" +
    "ПРИОРИТЕТНЫЕ ДЕЙСТВИЯ: (самые важные задачи на сейчас)\n" +
    "ДЕДЛАЙНЫ: (сроки по каждому этапу)\n" +
    "КОНТАКТЫ: (если упоминались в разговоре)\n" +
    "БЮДЖЕТ: (если обсуждался)\n" +
    "ВЕРОЯТНОСТЬ СДЕЛКИ: (в процентах)\n" +
    "Без воды, только факты и рекомендации.";

  try {
    const result = await model.generateContent(prompt);
    const text = result.response.text();
    logger.info("✅ Отчет получен от Gemini");
    return text;
  } catch (err) {
    logger.error(`Ошибка при анализе Gemini: ${err.message}`);
    throw err;
  }
}
